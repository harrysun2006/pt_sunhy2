function initcity(slcity) {
	var province = ($('#live_province').val()=='') ?  betterUser.live_province: $('#live_province').val();	
	$('#live_city').empty();
	if(province){
		for (i = 0 ;i <cityArray.length;i++)
        {   
               //得到 当前省 在 城市数组中的位置
			if(cityArray[i][0]==province)
            {   
                      //得到 当前省 所辖制的 地市
                tmpcityArray = cityArray[i][1].split("|")
                for(j=0;j<tmpcityArray.length;j++)
                {
                         //填充 城市 下拉选单   
                    tempcity = '';		
              		if(tmpcityArray[j]==slcity){
              			tempcity = 'selected';
              		}		
              		$('#live_city').append($("<option value='"+tmpcityArray[j]+"'"+tempcity+">"+tmpcityArray[j]+"</option>"));   
                 }
             }
        } 
	} 	
}
function creatprovince(province){
	$('#live_province').empty().append( $("<option value=0>"+betterLang.signup.province.tips+"</option>") );  
	for(var i = 0; i < provinces.length; i++) {	
		temppro = '';
		if(provinces[i]==province){
			temppro = 'selected';
		}
		$('#live_province').append($("<option value='"+provinces[i]+"'"+temppro+">"+provinces[i]+"</option>"));
	}
}
function checkprovince(province){
	result = false;		
	if($.inArray(province, provinces)>=0)
	{
		result = true;
	}
	return result;
}

function checkcity(tempcity){
	result = false;
	for (i = 0 ;i <cityArray.length;i++)
    {   
           //得到 当前省 在 城市数组中的位置
		if(cityArray[i][0]==$('#live_province').val())
        {   
                  //得到 当前省 所辖制的 地市
            tmpcityArray = cityArray[i][1].split("|")
            if($.inArray(tempcity, tmpcityArray)>=0)
			{
				result = true;
			}
        }
    }
	return result;
}

$(function(){
	try {
		days = betterLang.calendar.days.toString().split(',');
		months = betterLang.calendar.months.toString().split(',');
	}catch (e) {
	}
	creatprovince(betterUser.live_province);
	initcity(betterUser.live_city);
	$('#live_province').change(function(){		
		initcity();
	});
	$('#birthday').datepicker({
		changeMonth: true,
		changeYear: true,
		dateFormat: 'yy-mm-d'	,
		minDate : new Date(1900,1-1,1)	,
		yearRange : '1900:2010' , 
		dayNames : days,
		dayNamesMin :  days	,
		dayNamesShort :  days,
		monthNames : months,
		monthNamesShort : months,		
		defaultDate :  '-20y'
	});	

	$('#btnSaveBasic').click(function(){
		$('.err').empty();
		$('#set_basic_error').hide();
		$('#set_basic_success').hide();
		
		err = false;
		username = $.trim($('#username').val());
		email = $.trim($('#email').val());
		nickname = $.trim($('#nickname').val());
		self_intro = $.trim($('#self_intro').val());
		birthday = $('#birthday').val();
		live_province = $('#live_province').val();
		live_city = $('#live_city').val();
		patRT = /^RT(.*)/ig;
		patAT = /@/ig;
		patInvalid = /([\s\r\t ])/ig;
		patChinese = /[^\x00-\xff]/ig;
		patNoSpecialChar = /^[a-z0-9.]*$/gi;
		patEmail = /(\S)+[@]{1}(\S)+[.]{1}(\w)+/;
		patControllers = '/^('+patControllers+')$/i';
		
		if (!BETTER_USER_VALIDATING) {
			if (email.length==0) {
				$('#err_email').html(betterLang.signup.error.email.empty);
				err = true;
			} else if (!patEmail.test(email)) {
				$('#err_email').html(betterLang.signup.error.email.invalid);
				err = true;			
			}
		}
		checkp = 1;
		if(!checkprovince(live_province)){
			checkp = 0;
			$('#err_city').html(betterLang.signup.error.province.error);
			err = true;
		}
		if(!checkcity(live_city)){
			checkp  && $('#err_city').html(betterLang.signup.error.city.error);
			err = true;
		}
		if (username.length==0) {
			$('#err_username').html(betterLang.signup.error.regusername.empty);
			err = true;
		} else if (username.length>20) {
			$('#err_username').html(betterLang.signup.error.regusername.too_long);
			err = true;
		} else if (username.length<5) {
			$('#err_username').html(betterLang.signup.error.regusername.too_short);
			err = true;
		} else if (username.match(patAT)) {
			$('#err_username').html(betterLang.signup.error.regusername.forbidden_at);
			err = true;
		} else if (username.match(patInvalid)) {
			$('#err_username').html(betterLang.signup.error.regusername.forbidden_space);
			err = true;
		} else if(username.match(patChinese)) {
			$('#err_username').html(betterLang.signup.error.regusername.forbidden_chinese);
			err = true;
		} else if(!username.match(patNoSpecialChar)){
			$('#err_username').html(betterLang.signup.error.regusername.forbidden_specialChar);
			err = true;
		} else if(patControllers.match(username)){
			$('#err_username').html(betterLang.signup.error.regusername.already_taken);
			err = true;
		}
		
		if (nickname.length==0) {
			$('#err_nickname').html(betterLang.signup.error.nickname.empty);
			err = true;
		} else if (nickname.length>20) {
			$('#err_nickname').html(betterLang.signup.error.nickname.too_long);
			err = true;
		} else if (nickname.replace(/[^\x00-\xff]/ig, 'xxx').length<5) {
			$('#err_nickname').html(betterLang.signup.error.nickname.too_short);
			err = true;
		} else if (nickname.match(patAT)) {
			$('#err_nickname').html(betterLang.signup.error.nickname.no_at);
			err = true;
		} else if (nickname.match(patInvalid)) {
			$('#err_nickname').html(betterLang.signup.error.nickname.no_space);
			err = true;
		} 

		if(self_intro.length>50){
			$('#err_intro').html(betterLang.setting.basic.err.selfintro.toolong);
			err = true;
		}		
		posts = {
			todo: 'basic',
			username: username,
			nickname: nickname,
			gender: $('#gender_male').attr('checked') ? $('#gender_male').val() : ( $('#gender_female').attr('checked') ? $('#gender_female').val() : 'secret' ),
			self_intro: self_intro,
			birthday: birthday,
			live_province : live_province,
			live_city : live_city,
			language: $('#lang').val()
			};
		if (!BETTER_USER_VALIDATING) {
			posts.email = email;
		}
		
		if(err==false){
		Better_Notify_loading();
		
		$.post('/setting/update', posts, function(bsb_json){
			Better_Notify_clear();
			
			if (bsb_json.has_err=='1') {
				$('#set_basic_error').fadeIn();
				
				for(x in bsb_json.err) {
					$('#'+x).html(bsb_json.err[x]);
				}
			} else {
				if (betterUser.email!=$('#email').val()) {
					window.location = '/setting/needenable';
				} else {
					Better_Notify({
						msg: $('#set_basic_success').text()
					});
					
					if($('#gender_male').attr('checked') || $('#gender_female').attr('checked')){
						$('input[name=gender]').attr('disabled', true);
					}
				}
			}
		}, 'json');
		}
	});
	
	//不能修改性别
	if(betterUser.gender!='secret' && betterUser.gender!=''){
		$('input[name=gender]').attr('disabled', true);
	}
	
	
});